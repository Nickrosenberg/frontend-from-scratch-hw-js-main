function truncate(str, maxLength) {
  // Проверяем, превышает ли длина строки максимальную длину
  if (str.length > maxLength) {
      // Обрезаем строку до maxLength и добавляем многоточие
      return str.slice(0, maxLength) + '...';
  } else {
      // Если строка не превышает максимальную длину, возвращаем её как есть
      return str;
  }
}

// Примеры использования
console.log(truncate("Вот, что мне действительно нравится в этом", 20)); // "Вот, что мне действи..."
console.log(truncate("Короткая строка", 20)); // "Короткая строка"

